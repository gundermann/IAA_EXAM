package de.nak.cars.model;

public class Author {
	/** The identifier. */
	private Long keywordId;
	/** The authors name. */
	private String name;

	public Long getKeywordId() {
		return keywordId;
	}

	public void setKeywordId(Long keywordId) {
		this.keywordId = keywordId;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

}
